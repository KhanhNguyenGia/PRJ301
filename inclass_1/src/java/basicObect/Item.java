package basicObect;

/**
 *
 * @author jerry
 */
public class Item {

    private int id;
    private String name;
    private String image;
    private int price;

    public Item() {
        this.id = -1;
        this.name = "";
        this.image = "";
        this.price = -1;
    }

    public Item(int id, String name, String image, int price) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    
}
